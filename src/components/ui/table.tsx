import * as React from "react"

import { cn } from "@/lib/utils"

interface TableProps extends React.HTMLAttributes<HTMLTableElement> {
    tableName?: string; // Change the type to string
}

const Table = React.forwardRef<HTMLTableElement, TableProps>(
    ({ tableName, className, ...props }, ref) => {
        let heightClass = 'h-full'; // Default height

        if (tableName === 'attendance') {
            heightClass = 'h-[270px]'; // Height for attendance table
        } else if (tableName === 'birthday') {
            heightClass = 'h-[250px]'; // Height for offices table
        }  else if  (tableName === 'employee') {
            heightClass = 'h-[400px]  xs:h-[300px]';
        }else {
            heightClass = 'min-h-[10vh] max-h-[60vh]';
          
        }

        return (
            <div className={`relative w-full overflow-auto ${heightClass}`}>
                <table
                    ref={ref}
                    className={cn("w-full caption-bottom text-sm", className)}
                    {...props}
                />
            </div>
        );
    }
);
export default Table;

Table.displayName = "Table"

